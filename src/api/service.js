const EmployeeAPI = {
  // üìã –ù–∞—á–∞–ª—å–Ω—ã–π —Å–ø–∏—Å–æ–∫ —Å–æ—Ç—Ä—É–¥–Ω–∏–∫–æ–≤
  employees: [
    { number: 1, name: "Ben Blocker", job: "Teacher" },
    { number: 2, name: "Dave Defender", job: "Student" },
    { number: 3, name: "Sam Sweeper", job: "Teacher" },
    { number: 4, name: "Matt Midfielder", job: "Student" },
    { number: 5, name: "William Winger", job: "Student" },
    { number: 6, name: "Fillipe Forward", job: "Rector" },
  ],

  // üîπ –ü–æ–ª—É—á–∏—Ç—å –≤—Å–µ—Ö —Å–æ—Ç—Ä—É–¥–Ω–∏–∫–æ–≤
  all: function () {
    return this.employees;
  },

  // üîç –ù–∞–π—Ç–∏ —Å–æ—Ç—Ä—É–¥–Ω–∏–∫–∞ –ø–æ ID
  get: function (id) {
    return this.employees.find((p) => p.number === id);
  },

  // üóëÔ∏è –£–¥–∞–ª–∏—Ç—å —Å–æ—Ç—Ä—É–¥–Ω–∏–∫–∞ –ø–æ ID
  delete: function (id) {
    this.employees = this.employees.filter((p) => p.number !== id);
  },

  // ‚ûï –î–æ–±–∞–≤–∏—Ç—å –Ω–æ–≤–æ–≥–æ —Å–æ—Ç—Ä—É–¥–Ω–∏–∫–∞
  add: function (employee) {
    this.employees.push(employee); // push –¥–æ–±–∞–≤–ª—è–µ—Ç –≤ –∫–æ–Ω–µ—Ü –º–∞—Å—Å–∏–≤–∞
    return employee;
  },

  // üîÑ –û–±–Ω–æ–≤–∏—Ç—å –¥–∞–Ω–Ω—ã–µ —Å–æ—Ç—Ä—É–¥–Ω–∏–∫–∞
  update: function (employee) {
    const index = this.employees.findIndex((p) => p.number === employee.number);
    if (index !== -1) {
      this.employees[index] = employee; // –∑–∞–º–µ–Ω—è–µ–º –ø–æ –∏–Ω–¥–µ–∫—Å—É
    }
    return employee;
  },
};

